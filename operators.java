public class operators {
    public static void main(String[] args) {

        // operators = +, -, /, %
        // operands = values, variables, numbers, quantities
        // expresion = operands + operators
        
        int a = 5;

        a = a + 1;
        System.out.println(a);
        
        a ++;
        System.out.println(a);
        
        a --;
        System.out.println(a);


        double b = 10;
        b = b / 3;
        System.out.println(b);


    }
}
